class Vector:
    def __init__(self, x: float, y: float, z: float) -> None:
        self.x = x
        self.y = y
        self.z = z

    def getX(self) -> float:
        return self.x
        
    def getY(self) -> float:
        return self.y
    
    def getZ(self) -> float:
        return self.z
